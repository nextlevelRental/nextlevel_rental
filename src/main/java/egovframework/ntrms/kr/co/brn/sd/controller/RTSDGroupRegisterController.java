	@RequestMapping("/rtms/sd/listAddGroupCommInfo.do") 
	public ModelAndView listCommInfo(NexacroMapDTO xpDto, Model model) throws Exception {
		ModelAndView mav = new ModelAndView("nexacroMapView");
		try {
			Map <String, Object> inVar 			= xpDto.getInVariableMap();
			Map <String, DataSetMap> outDataset = xpDto.getOutDataSetMap();
			
			Map com = new HashMap();
			com.put("cdGrpCd", "S202"); //단체상태
			com.put("useYn", "Y");
			Map commMap = commonService.listCommInfo(com);
			List listComm	= (List)commMap.get("result");
			DataSetMap dsGrp = new DataSetMap();
			dsGrp.setRowMaps(listComm);
			outDataset.put("com_grp", dsGrp);

			com.put("cdGrpCd", "S204");	//단체유형
			com.put("useYn", "Y");
			commMap = commonService.listCommInfo(com);
			List listComm204 = (List)commMap.get("result");
			DataSetMap dsType = new DataSetMap();
			dsType.setRowMaps(listComm204);
			outDataset.put("com_type",  dsType);
			
			mav.addObject(NexacroConstant.OUT_VARIABLES_ATT_NAME, 	xpDto.getOutVariableMap());
			mav.addObject(NexacroConstant.OUT_DATASET_ATT_NAME, 	xpDto.getOutDataSetMap());
			mav.addObject(NexacroConstant.ERROR_CODE, "0");
			mav.addObject(NexacroConstant.ERROR_MSG, "");
		
		} catch ( Exception e ) {
			e.printStackTrace();
			mav.addObject(NexacroConstant.ERROR_CODE, "-1");
			mav.addObject(NexacroConstant.ERROR_MSG, e.toString());
		}
		return mav;
	}
	
